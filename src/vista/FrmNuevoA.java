    private void llenarComboObjetos() {
        AlineacionDAO aDAO = new AlineacionDAO();  // Crear instancia del DAO

        comboBase.removeAllItems(); // Limpiar el ComboBox antes de llenarlo
        comboAlero.removeAllItems();
        comboEscolta.removeAllItems();
        comboAPivot.removeAllItems();
        comboPivot.removeAllItems();

        List<Perfil> bases = aDAO.obtenerJugadoresPorPosicion("BASE");
        for (Perfil perfil : bases) {
            comboBase.addItem(perfil.getNombre());
        }

        List<Perfil> aleros = aDAO.obtenerJugadoresPorPosicion("ALERO");
        for (Perfil perfil : aleros) {
            comboAlero.addItem(perfil.getNombre());
        }

        List<Perfil> escoltas = aDAO.obtenerJugadoresPorPosicion("ESCOLTA");
        for (Perfil perfil : escoltas) {
            comboEscolta.addItem(perfil.getNombre());
        }

        List<Perfil> apivots = aDAO.obtenerJugadoresPorPosicion("ALA PIVOT");
        for (Perfil perfil : apivots) {
            comboAPivot.addItem(perfil.getNombre());
        }

        List<Perfil> pivots = aDAO.obtenerJugadoresPorPosicion("PIVOT");
        for (Perfil perfil : pivots) {
            comboPivot.addItem(perfil.getNombre());
        }
    }
